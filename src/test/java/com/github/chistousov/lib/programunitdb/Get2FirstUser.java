package com.github.chistousov.lib.programunitdb;

import com.github.chistousov.lib.programunitdb.annotations.Column;
import com.github.chistousov.lib.programunitdb.annotations.OutParam;

@OutParam(name = "REF_CURSOR")
public class Get2FirstUser {
    
    @Column(name = "id")
    private Long id;
    private String name; 

    public Long getId() {
        return id;
    }
    public void setId(Long id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(@Column(name = "name") String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        return result;
    }
    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Get2FirstUser other = (Get2FirstUser) obj;
        if (id == null) {
            if (other.id != null)
                return false;
        } else if (!id.equals(other.id))
            return false;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        return true;
    }

    
}
